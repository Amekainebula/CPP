void solve()
{
    int n, m, k;
    cin >> n >> m >> k;
    int l = 0, r = m;
    auto check = [&](int mid)
    {
        return (m / (mid + 1) * mid + m % (mid + 1)) * n >= k;
    };
    while (l < r)
    {
        int mid = (l + (r - l)) >> 1;
        if (check(mid))
            r = mid;
        else
            l = mid + 1;
    }
    cout << l << endl;
}
