void solve()
{
    int n, m;
    cin >> n >> m;
    vector<int> a(n + 1), b(m + 1);
    for (int i = 1; i <= n; i++)
        cin >> a[i];
    for (int i = 1; i <= n; i++)
        cin >> b[i];
    map<int, int> mp;
    for (int i = 1; i <= n; i++)
    {
        int l = a[i] - b[i], r = a[i] + b[i];
        for (int j = l; j <= r; j++)
            mp[j] = max(mp[j], (int)sqrt(b[i] * b[i] - (j - a[i]) * (j - a[i])));
    }
    int ans = 0;
    for (auto [_, x] : mp)
        ans += 2 * x + 1;
    cout << ans << endl;
}
