void solve()
{
    int n, k, x;
    int sum = 0;
    cin >> n >> k >> x;
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
    }
    for (int i = 1; i <= n; i++)
    {
        sum += a[i];
    }
    int tep = x / sum;
    x %= sum;
    int now = n;
    while (x > 0)
        x -= a[now--];
    cout << max((int)(k - tep - 1) * n + now + 1, 0LL) << endl;
}

